numbers = [5, 6, 7, 8]
print(numbers)

numbers.append(99) #Untuk menambahkan angka di list yg paling belakang
print(numbers)

numbers.insert(2, 100) #Untuk menambahkan angka diantara angka, 2= angka ke 2, 100 = bilangan yang akan dimasukan
print(numbers)

numbers.pop(5) #Untuk menghapus angka ke 5
print(numbers)

numbers.remove(8) #Untuk menghapus angka secara langsung (tinggal input)
print(numbers)

numbers.sort() #Untuk mengurutkan angka
print(numbers)

numbers.reverse() #Untuk mengurutkan angka dari kanan ke kiri
print(numbers)
