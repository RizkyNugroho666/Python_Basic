print("Untuk mencetak angka 1-10 dari atas ke bawah")
#Untuk mencetak angka 1 - 10
for item in range(1, 11): #harus di isi +1 dari angka yang kita inginkan
    print(item)

print("Untuk mencetak angka 1-10 dari atas ke bawah")
#Untuk mencetak angka dengan lompatan bilangan
for item in range(1, 11, 2): #angka 2 adalah jumlah lompatan dari satu angka ke angka lain
    print(item)